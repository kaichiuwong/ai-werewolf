// Default setup for different player counts
export const getRoleDistribution = (count: number): Role[] => {
  const roles: Role[] = [];
  
  // Base setup for 6 players
  roles.push(Role.WEREWOLF, Role.WEREWOLF);
  roles.push(Role.SEER);
  roles.push(Role.WITCH);
  roles.push(Role.VILLAGER, Role.VILLAGER);

  // 7-12
  if (count >= 7) roles.push(Role.VILLAGER);
  if (count >= 8) roles.push(Role.HUNTER); 
  if (count >= 9) roles.push(Role.WEREWOLF);
  if (count >= 10) roles.push(Role.VILLAGER);
  if (count >= 11) roles.push(Role.VILLAGER);
  if (count >= 12) roles.push(Role.WEREWOLF);

  // 13-18 (Extended support)
  if (count >= 13) roles.push(Role.VILLAGER);
  if (count >= 14) roles.push(Role.WEREWOLF); // 5 Wolves total
  if (count >= 15) roles.push(Role.VILLAGER);
  if (count >= 16) roles.push(Role.VILLAGER);
  if (count >= 17) roles.push(Role.VILLAGER);
  if (count >= 18) roles.push(Role.WEREWOLF); // 6 Wolves total

  return roles.slice(0, count);
};

